#!/usr/bin/env node
// Assemble the demo (SPEC-077): vendor the freshly built `pkg/` into `demo/vendor/`
// so the page can `import` it directly — no bundler, no npm install, no toolchain
// beyond `just wasm-build` and this copy.
//
// THE ASSEMBLY GUARD. The demo deploys a .wasm to the public internet, so the one
// thing this script must not let through is a .wasm that did NOT come through the
// size-profiled `just wasm-build` (DEC-066) — a bare `cargo build --target wasm32`
// produces a working-but-fat artifact that would silently cost every visitor
// +109 KB on the wire. It is caught STRUCTURALLY, by the `strip` fingerprint in the
// binary itself, not by a size band (see scripts/lib/wasm-artifact.mjs for why).
//
// Run: `just demo-build`.

import { copyFileSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { brotliCompressSync } from "node:zlib";

import {
  WASM_BROTLI_BASELINE,
  WASM_BROTLI_MAX,
  WASM_BROTLI_MIN,
  WASM_BROTLI_TOLERANCE,
  WASM_NAME_SECTION_MAX,
  nameSectionSize,
} from "./lib/wasm-artifact.mjs";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const pkgDir = join(repoRoot, "pkg");
const vendorDir = join(repoRoot, "demo", "vendor");

// Only what the page actually loads. The .d.ts files and package.json are npm's
// concern (SPEC-075), not a static page's.
const VENDORED = ["crustyimg.js", "crustyimg_bg.wasm"];

function die(msg) {
  console.error(`\ndemo-assemble: ${msg}\n`);
  process.exit(1);
}

let wasm;
try {
  wasm = readFileSync(join(pkgDir, "crustyimg_bg.wasm"));
} catch {
  die("no pkg/crustyimg_bg.wasm — run `just wasm-build` first (the `demo-build` recipe does)");
}

// ── the guard ─────────────────────────────────────────────────────────────────

const nameSection = nameSectionSize(wasm);
const brotli = brotliCompressSync(wasm).length;

if (nameSection > WASM_NAME_SECTION_MAX) {
  die(
    `the .wasm in pkg/ is NOT the size-profiled build: it carries a ${nameSection} B \`name\` debug ` +
      `section (a stripped, profiled artifact carries <= ${WASM_NAME_SECTION_MAX} B; a stock-profile ` +
      `one carries ~980 KB). Build it through \`just wasm-build\`, never a bare ` +
      `\`cargo build --target wasm32-unknown-unknown\` — DEC-066.`,
  );
}

if (brotli > WASM_BROTLI_MAX || brotli < WASM_BROTLI_MIN) {
  die(
    `the .wasm is ${brotli} B brotli, outside ${WASM_BROTLI_TOLERANCE * 100}% of the ` +
      `${WASM_BROTLI_BASELINE} B baseline. Over: a real size regression — every visitor pays it, so ` +
      `move the baseline deliberately and say why. Under: the AVIF encoder is probably missing ` +
      `(DEC-065), i.e. this was built without --features avif.`,
  );
}

// ── the copy ──────────────────────────────────────────────────────────────────

rmSync(vendorDir, { recursive: true, force: true });
mkdirSync(vendorDir, { recursive: true });

for (const f of VENDORED) {
  copyFileSync(join(pkgDir, f), join(vendorDir, f));
}

// The vendor dir is a build output — regenerated by every assemble, never edited,
// never committed. Say so where someone will actually see it.
writeFileSync(
  join(vendorDir, "README.md"),
  "# vendored build output — do not edit, do not commit\n\n" +
    "Copied from `pkg/` by `scripts/demo-assemble.mjs` (`just demo-build`), which builds it\n" +
    "through the size-profiled `just wasm-build` (DEC-066). Regenerated from scratch every time.\n",
);

const version = JSON.parse(readFileSync(join(pkgDir, "package.json"), "utf8")).version;
console.log(
  `demo-assemble: vendored crustyimg-wasm ${version} → demo/vendor/ ` +
    `(${(wasm.length / 1048576).toFixed(2)} MB raw, ${(brotli / 1048576).toFixed(2)} MB brotli; ` +
    `\`name\` section ${nameSection} B → size-profiled ✓)`,
);
